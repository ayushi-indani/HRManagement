    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id=jTextField1.getText();
        String pwd=jPasswordField1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hrmanagement","root","root");
            PreparedStatement ps=con.prepareStatement("select * from hr where hr_id=? and password=?");
            ps.setString(1, id);
            ps.setString(2,pwd);
            ResultSet rs=ps.executeQuery();      
            int x=0;
            if(rs.next())
            {
                x=1;
                JOptionPane.showMessageDialog(this,"Login Success");
                dispose();
                new hr_home(jTextField1.getText()).setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(this,"Login Failed");
          }catch(Exception e)
            {
            JOptionPane.showMessageDialog(this,e);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

